package com.app.mobiledev.salesapp.aktifitas;

public class aktifitasModel {

    private String kyano;
    private String aktifitas;
    private String note;
    private String tgl;

    public String getTgl() {
        return tgl;
    }

    public void setTgl(String tgl) {
        this.tgl = tgl;
    }

    public String getKyano() {
        return kyano;
    }

    public void setKyano(String kyano) {
        this.kyano = kyano;
    }

    public String getAktifitas() {
        return aktifitas;
    }

    public void setAktifitas(String aktifitas) {
        this.aktifitas = aktifitas;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
